    private void jBtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSearchActionPerformed
        // TODO add your handling code here:
        TableDetails();
        int checkid=0;
        String id=jTextFieldStId.getText();
        String month=jLabelDate2.getText();
        try
        {
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where id='"+id+"'");
            while(rs.next())
            {
                checkid=1;
                jTextFieldStId.setEditable(false);
                jTextFieldName.setText(rs.getString(2));
                jTextFieldMN.setText(rs.getString(4));
                jTextFieldEmail.setText(rs.getString(5));
            }
            
            if(checkid==1)
            {
                
                ResultSet rs1=st.executeQuery("select * from fee inner join student where fee.month='"+month+"' and fee.id='"+id+"' and student.id='"+id+"'");
                while(rs1.next())
                {
                    jButtonSave.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Payment is already done for this Month");
                }
            }
            else
            {
              JOptionPane.showMessageDialog(null, "Student ID does not Exist");
           }
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jBtnSearchActionPerformed

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String id=jTextFieldStId.getText();
        String month=jLabelDate2.getText();
        String fee=jTextFieldFee.getText();
        try
        {
            Connection con=ConnectionProvider.getcon();
            PreparedStatement ps=con.prepareStatement("insert into fee values(?,?,?)");
            ps.setString(1, id);
            ps.setString(2, month);
            ps.setString(3, fee);
            ps.executeUpdate();
            TableDetails();
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new ClassPayment().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

