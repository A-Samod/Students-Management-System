    public void TableDetails()
    {
        DefaultTableModel dtm=(DefaultTableModel)jTableFee.getModel();
        dtm.setRowCount(0);
        String id=jTextFieldStId.getText();
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from fee where id='"+id+"'");
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString(2),rs.getString(3)});
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
